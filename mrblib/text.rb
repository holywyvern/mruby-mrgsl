module MRGSL
  #------------------------------------------------------------------------------------------
  # * Text class
  #------------------------------------------------------------------------------------------
  class Text
    #----------------------------------------------------------------------------------------
    # * Accessors
    #----------------------------------------------------------------------------------------
    attr_accessor  :text
    attr_accessor  :font
    attr_accessor  :decorator
    #---------------------------------------------------------------------------------------
    # * Constructor
    #----------------------------------------------------------------------------------------
    def initialize(text= nil, font =nil, decorator =nil)
      set(text, font, decorator)
    end

    #----------------------------------------------------------------------------------------
    # * set
    #----------------------------------------------------------------------------------------
    def set(text =nil, font =nil, decorator =nil)
      @text = text
      @font = font
      @decorator = decorator
    end
  end
end